package com;
public class Aspects{
	String review;
	String aspect_term;
	String opinion_word;
	String pair;
public void setReview(String review){
	this.review = review;
}
public String getReview(){
	return review;
}

public void setAspectTerm(String aspect_term){
	this.aspect_term = aspect_term;
}
public String getAspectTerm(){
	return aspect_term;
}

public void setOpinionWord(String opinion_word){
	this.opinion_word = opinion_word;
}
public String getOpinionWord(){
	return opinion_word;
}

public void setPair(String pair){
	this.pair = pair;
}
public String getPair(){
	return pair;
}
}